package New2026.String;

public class StringBasics {

    public static void main (String[] args){
        //character Array of size 5
        char[] arr = new char[5];
        arr[0] = 'a';
        arr[1] = 'b';
        arr[2] = 'c';
        arr[3] = 'd';
        arr[4] = 'e';

        for (char c : arr) {
            System.out.println(c);
        }


    }
}
